package laboratorio;

public class Practica {
	
	private int codigo;
	private String nombrePractica;
	private String grupo;
	private int valoresCriticos;
	private int valoresReservados;
	private int tiempoDePractica;
	
	
	public int getCodigo() {
		return codigo;
	}
	public void setCodigo(int codigo) {
		this.codigo = codigo;
	}
	public String getNombrePractica() {
		return nombrePractica;
	}
	public void setNombrePractica(String nombrePractica) {
		this.nombrePractica = nombrePractica;
	}
	public String getGrupo() {
		return grupo;
	}
	public void setGrupo(String grupo) {
		this.grupo = grupo;
	}
	public int getValoresCriticos() {
		return valoresCriticos;
	}
	public void setValoresCriticos(int valoresCriticos) {
		this.valoresCriticos = valoresCriticos;
	}
	public int getValoresReservados() {
		return valoresReservados;
	}
	public void setValoresReservados(int valoresReservados) {
		this.valoresReservados = valoresReservados;
	}
	public int getTiempoDePractica() {
		return tiempoDePractica;
	}
	public void setTiempoDePractica(int tiempoDePractica) {
		this.tiempoDePractica = tiempoDePractica;
	}
}
